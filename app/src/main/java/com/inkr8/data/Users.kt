package com.inkr8.data

data class Users(
    val id: String = "",
    val name: String = "",
    val email: String? = null,
    var merit: Long = 1000,
    val meritCap: Long = 50000,
    val meritHold: Long = 0,
    val rating: Long = 0,
    val reputation: Long = 0,
    val bestScore: Double = 0.0,
    val submissionsCount: Long = 0,
    val savedSubmissionsCount: Long = 0,
    val profileImageURL: String = "",
    val bannerImageURL: String = "",
    val achievements: List<String> = emptyList(),
    val joinedDate: Long = System.currentTimeMillis(),
    val rankedWinStreak: Long = 0,
    val rankedLossStreak: Long = 0,
    val currentlyInRanked: Boolean = false,
    val rankedSessionStartedAt: Long? = null,
    val tournamentsPlayed: Long = 0,
    val tournamentsWon: Long = 0,
    val totalMeritEarned: Long = 0,
    val tipsReceived: Long = 0,
    val isPhilosopher: Boolean = false,
    val philosopherSince: Long? = null,
    val hasChosenUsername: Boolean = false,
    val recentScores: List<Double> = emptyList()
)
